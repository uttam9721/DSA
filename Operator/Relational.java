package Operator;

public class Relational {
    public static void main(String[] args) {
        // == , ! , > , < , <= , >= always return true or false value;
        int a = 10, b=20;
        System.out.println(a==b); // false
        System.out.println(a!=b); // true
        System.out.println(a>b); // false
        System.out.println(a<b); // true
        System.out.println(a<=b); // true
        System.out.println(a>=b); // false
        
    }
    
}
